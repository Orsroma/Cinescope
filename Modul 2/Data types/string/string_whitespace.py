#         2. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –æ—Å—Ç–∞–≤–∏—Ç—å –ø—Ä–æ–±–µ–ª—ã —Ç–æ–ª—å–∫–æ —Å –æ–¥–Ω–æ–π —Å—Ç–æ—Ä–æ–Ω—ã —Å—Ç—Ä–æ–∫–∏ (–Ω–∞–ø—Ä–∏–º–µ—Ä, —Å –ø–æ–º–æ—â—å—é `lstrip()` –∏–ª–∏ `rstrip()`), —á—Ç–æ–±—ã –ø–æ–Ω—è—Ç—å —Ä–∞–∑–Ω–∏—Ü—É –≤ –≤—ã–≤–æ–¥–µ.


text = "  –£—á–∏–º—Å—è Python.   "

