greeting = "   –î–æ–±—Ä—ã–π –¥–µ–Ω—å!   "
cleaned_greeting = greeting.strip()
left_trimmed = greeting.lstrip()
right_trimmed = greeting.rstrip()

print(cleaned_greeting)
print(left_trimmed)
print(right_trimmed)
